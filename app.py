class Application(Frame):
    """一个经典的GUI类的写法"""
    def __init__(self, master=None, bgcolor='#000000'):
        super().__init__(master)
        self.master = master
        # self.bgcolor = bgcolor
        self.pack()
        self.StartDrawFlag = False
        self.DrawButtonText = ['开始绘制', '结束绘制']
        self.x, self.y = 0, 0       # 存储鼠标坐标
        self.lastDraw = 0           # 上次绘制的图形
        self.cvWIDTH, self.cvHEIGHT = 100, 100
        self.linewidth = 6
        self.ImageMatrix = np.ones((self.cvWIDTH + 4*self.linewidth, self.cvHEIGHT + 4*self.linewidth))

        self.creatWidget()
        self._28x28Mat = 0
        self.mynetwork = MNISTNetwork()
